package eu.cubixmc.experienceapi;

public class PlayerData {
	
	private int exp;
	private int level;

	public int getExp() {
		return exp;
	}

	public void setExp(int exp) {
		this.exp = exp;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}
	

}
